#include <unistd.h>

int	main(int argc, char const **argv)
{
	int	i;
	int	h;

	i = argc - 1;
	while (i > 0)
	{
		h = 0;
		while (argv[i][h])
		{
			write(1, &argv[i][h], 1);
			h++;
		}
		write(1, "\n", 1);
		i--;
	}
	return (0);
}
