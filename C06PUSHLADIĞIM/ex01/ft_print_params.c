#include <unistd.h>

int	main(int argc, char **argv)
{
	int	i;
	int	h;

	i = 1;
	while (i < argc)
	{
		while (argv[i][h])
		{
			write(1, &argv[i][h], 1);
			h++;
		}
		write(1, "\n", 1);
		i++;
		h = 0;
	}
	return (0);
}
